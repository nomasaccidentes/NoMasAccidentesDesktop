     public DefaultTableModel showData(){
        PagoContratoDetalleService  contratoDetalleService = new PagoContratoDetalleService();
        DefaultTableModel dtm = new DefaultTableModel();
        
        dtm.addColumn("Id");
        dtm.addColumn("Descripcion");
        dtm.addColumn("Pago Contrato");
        
        String array = contratoDetalleService.getPagoDetalleByPagoId(Integer.parseInt(idPago));        
        JSONObject obj = new JSONObject(array);        
        Usuario u = new Usuario();
        JSONArray data = obj.getJSONArray("data");               
        for (int i = 0; i < data.length(); i++) {
                JSONObject row = data.getJSONObject(i);
                dtm.addRow(new Object[]{row.getInt("PAGO_CONTRATO_DETALLE_ID"),row.get("PAGO_CONTRATO_DETALLE_DESC"), row.getInt("PAGO_CONTRATO_ID")});
        }
        
        return dtm;
        
    }

