    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        RegistroAccidente accidente = new RegistroAccidente();
        
        accidente.contratoId = contratoId;
        accidente.profesionalId = cmbEncargado.getItemAt(cmbEncargado.getSelectedIndex()).getProfesional_id();
        
         SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String startDateString = dateFormat.format(dtpFechaRegistroAccidente.getDate());
        accidente.registroAccidenteFecha = startDateString;
        
        accidente.registroAccidenteNombre = descripcionRegistroAccidente.getText();
        accidente.asesoriaId = asesoriaId;
        
        RegistroAccidenteService accidenteService = new RegistroAccidenteService();
        try {
            accidenteService.postRegistroAccidente(accidente);
            JOptionPane.showMessageDialog(null, "Registro Accidente Insertado");
        } catch (Exception ex) {
            Logger.getLogger(MantenedorInsertRegistroAcciente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

