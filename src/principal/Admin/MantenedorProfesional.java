    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        Profesional p = new Profesional();
        
        p.profesional_nombre = txtNombreProfesionalInsert.getText();
        p.profesional_apellido = txtApellidoProfesionalInsert.getText();
        p.profesional_rut = txtRutProfesionalInsert.getText();
        int activo = 0;
        if(radioActivoProfesionalInsert.isSelected()){
            activo = 1;
        }
        
        p.profesional_activo = activo;
        
        ProfesionalService ps = new ProfesionalService();
        
        try {
            ps.postProfesional(p);
            JOptionPane.showMessageDialog(null, "Profesional registrado correctamente");
        } catch (Exception ex) {
            Logger.getLogger(MantenedorProfesional.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

