    public DefaultTableModel showData(){
        ProfesionalService ps = new ProfesionalService();
        DefaultTableModel dtm = new DefaultTableModel();
        
        dtm.addColumn("Id");
        dtm.addColumn("Nombre");
        dtm.addColumn("Apellido");
        dtm.addColumn("Rut");
        dtm.addColumn("Creacion");
        dtm.addColumn("Estado");
        
        JSONArray array = ps.getProfesional();
        for (int i = 0; i < array.length(); i++) {
                JSONObject row = array.getJSONObject(i);
                dtm.addRow(new Object[]{row.getInt("PROFESIONAL_ID"),row.getString("PROFESIONAL_NOMBRE"), row.get("PROFESIONAL_APELLIDO"), row.getString("PROFESIONAL_RUT"), row.getString("PROFESIONAL_TS_CREACION"), row.getInt("PROFESIONAL_ACTIVO")});
            }
        
        return dtm;
        
    }

