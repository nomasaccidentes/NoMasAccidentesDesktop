    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        int estado = 0;
        if(cmbEstadoPago.getSelectedItem().equals("Pendiente")){
            estado = 1;
        }else if(cmbEstadoPago.getSelectedItem().equals("Aprobado")){
            estado = 2;
        }else if(cmbEstadoPago.getSelectedItem().equals("Rechazado")){
            estado = 3;
        }
        
        
        PagoContrato contrato = new PagoContrato();
        
        contrato.pagoContratoEstadoId = estado;
        
                
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String startDateString = dateFormat.format(jDateFechaPagoReal.getDate());
        
        contrato.pagoFechaReal = startDateString;
        
        
        PagoContratoService contratoService = new PagoContratoService();
        try {
            contratoService.ingresaPagoContrato(contrato, Integer.parseInt(pagoId));
            JOptionPane.showMessageDialog(null, "Pago Editado Correctamente");
            this.setVisible(false);
            MantenedorVerPagos mantenedorVerPagos = new MantenedorVerPagos(idContrato);
            mantenedorVerPagos.setVisible(true);
        } catch (Exception ex) {
            Logger.getLogger(MantenedorVerPagos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

