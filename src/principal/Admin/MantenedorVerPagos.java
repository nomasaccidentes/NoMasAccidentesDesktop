     public DefaultTableModel showData(){
         PagoContratoService  contratoService = new PagoContratoService();
        DefaultTableModel dtm = new DefaultTableModel();
        
        dtm.addColumn("Id");
        dtm.addColumn("Descripcion");
        dtm.addColumn("Fecha Vcto");
        dtm.addColumn("Fecha Pago Real");     
        dtm.addColumn("Estado Pago");
        
        String array = contratoService.getPagoContratoById(Integer.parseInt(idContrato));        
        JSONObject obj = new JSONObject(array);        
        Usuario u = new Usuario();
        JSONArray data = obj.getJSONArray("data");               
        for (int i = 0; i < data.length(); i++) {
                JSONObject row = data.getJSONObject(i);
                String estado = "";
                if(row.getInt("PAGO_CONTRATO_ESTADO_ID") == 1){
                    estado = "Pendiente";
                }else if(row.getInt("PAGO_CONTRATO_ESTADO_ID") == 2){
                    estado = "Aprobado";
                }else if(row.getInt("PAGO_CONTRATO_ESTADO_ID") == 3){
                    estado = "Rechazado";
                }
                dtm.addRow(new Object[]{row.getInt("PAGO_CONTRATO_ID"),row.get("PAGO_CONTRATO_DESCRIPCION"), row.get("PAGO_FECHA_VCTO"), row.get("PAGO_FECHA_PAGO"),estado });
        }
        
        return dtm;
        
    }
